package com.callor.var;

public class Varriable_05 {

	public static void main(String[] args) {

		int intNum = 1;
		int intSum = 0;
		
		intSum += intNum;
		System.out.println(intSum);
		System.out.println(intNum);
		
		intNum ++;
		intSum += intNum;
		System.out.println(intNum);
		System.out.println(intSum);
		
		intNum ++;
		intSum += intNum;
		System.out.println(intNum);
		System.out.println(intSum);
		
		
		
		

	}

}
